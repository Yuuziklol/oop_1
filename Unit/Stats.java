package Unit;
public class Stats{
    protected int hp;
    protected int attack;
    protected int def;
    protected int mana;
    protected int speed;
    protected int intellect;
    protected int hit;
    public Stats(int hp, int attack, int def, int mana, int speed, int intellect, int hit) {
        this.hp = hp;
        this.attack = attack;
        this.def = attack;
        this.mana = mana;
        this.speed = speed;
        this.intellect = intellect;
        this.hit = hit;
    }
    
}